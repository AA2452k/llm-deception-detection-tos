class PerformanceAnalyzer:
    def __init__(self, results_df: pd.DataFrame):
        self.results_df = results_df
        self.metrics = {}

    def calculate_metrics(self):
        """Calculate all performance metrics"""
        # Filter out unclear predictions for metrics calculation
        valid_no_cot = self.results_df[self.results_df['no_cot_prediction'] != 'unclear']
        valid_cot = self.results_df[self.results_df['cot_prediction'] != 'unclear']

        # No-CoT metrics
        if len(valid_no_cot) > 0:
            self.metrics['no_cot'] = {
                'accuracy': accuracy_score(valid_no_cot['true_label'], valid_no_cot['no_cot_prediction']),
                'precision': precision_score(valid_no_cot['true_label'], valid_no_cot['no_cot_prediction'],
                                           pos_label='deceptive', average='binary'),
                'recall': recall_score(valid_no_cot['true_label'], valid_no_cot['no_cot_prediction'],
                                     pos_label='deceptive', average='binary'),
                'f1': f1_score(valid_no_cot['true_label'], valid_no_cot['no_cot_prediction'],
                             pos_label='deceptive', average='binary'),
                'unclear_count': len(self.results_df[self.results_df['no_cot_prediction'] == 'unclear'])
            }

        # CoT metrics
        if len(valid_cot) > 0:
            self.metrics['cot'] = {
                'accuracy': accuracy_score(valid_cot['true_label'], valid_cot['cot_prediction']),
                'precision': precision_score(valid_cot['true_label'], valid_cot['cot_prediction'],
                                           pos_label='deceptive', average='binary'),
                'recall': recall_score(valid_cot['true_label'], valid_cot['cot_prediction'],
                                     pos_label='deceptive', average='binary'),
                'f1': f1_score(valid_cot['true_label'], valid_cot['cot_prediction'],
                             pos_label='deceptive', average='binary'),
                'unclear_count': len(self.results_df[self.results_df['cot_prediction'] == 'unclear'])
            }

        return self.metrics

    def print_metrics(self):
        """Print formatted metrics"""
        print("="*50)
        print("PERFORMANCE METRICS")
        print("="*50)

        for method in ['no_cot', 'cot']:
            if method in self.metrics:
                print(f"\n{method.upper().replace('_', '-')} Performance:")
                print("-"*30)
                for metric, value in self.metrics[method].items():
                    if metric != 'unclear_count':
                        print(f"{metric.capitalize():12s}: {value:.3f}")
                    else:
                        print(f"Unclear predictions: {value}")

        # Calculate improvement
        if 'no_cot' in self.metrics and 'cot' in self.metrics:
            print("\n" + "="*50)
            print("IMPROVEMENT WITH CoT")
            print("="*50)
            for metric in ['accuracy', 'precision', 'recall', 'f1']:
                improvement = self.metrics['cot'][metric] - self.metrics['no_cot'][metric]
                percent_improvement = (improvement / self.metrics['no_cot'][metric]) * 100 if self.metrics['no_cot'][metric] > 0 else 0
                print(f"{metric.capitalize():12s}: {improvement:+.3f} ({percent_improvement:+.1f}%)")

