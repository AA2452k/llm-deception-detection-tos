"""
### 4. Running the Experiment
Evaluating all clauses with both CoT and non-CoT prompts
"""

class ExperimentRunner:
    def __init__(self, evaluator: LLMEvaluator, dataset: pd.DataFrame):
        self.evaluator = evaluator
        self.dataset = dataset
        self.results = []

    def run_experiment(self):
        """Run the full experiment"""
        print("Starting experiment...")

        for idx, row in tqdm(self.dataset.iterrows(), total=len(self.dataset)):
            clause = row['clause']
            true_label = row['label']

            # Evaluate without CoT
            no_cot_result = self.evaluator.evaluate_clause(clause, use_cot=False)

            # Evaluate with CoT
            cot_result = self.evaluator.evaluate_clause(clause, use_cot=True)

            # Store results
            self.results.append({
                'clause': clause,
                'true_label': true_label,
                'no_cot_prediction': no_cot_result['classification'],
                'no_cot_response': no_cot_result['response'],
                'cot_prediction': cot_result['classification'],
                'cot_response': cot_result['response']
            })

            # Save intermediate results
            if (idx + 1) % 5 == 0:
                self.save_results(f"intermediate_results_{idx+1}.json")

        print("Experiment completed!")
        return pd.DataFrame(self.results)

    def save_results(self, filename: str):
        """Save results to file"""
        with open(filename, 'w') as f:
            json.dump(self.results, f, indent=2)

