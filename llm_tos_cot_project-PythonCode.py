class LLMEvaluator:
    def __init__(self, model_name="meta-llama/Meta-Llama-3-8B", cache_dir=None):
        """
        Initialize the LLM evaluator
        :param model_name: Name of the LLM model to load
        :param cache_dir: Directory to cache model files
        """
        if cache_dir and not os.path.exists(cache_dir):
            os.makedirs(cache_dir)
            print(f"Created cache directory: {cache_dir}")

        print(f"Loading model: {model_name}")
        if cache_dir:
            print(f"Using cache directory: {cache_dir}")

        self.model_name = model_name
       # self.tokenizer = AutoTokenizer.from_pretrained(model_name)
        self.tokenizer = AutoTokenizer.from_pretrained(
            model_name,
            cache_dir=cache_dir
        )
        # Add padding token if not present
        if self.tokenizer.pad_token is None:
            self.tokenizer.pad_token = self.tokenizer.eos_token

        # Load model with appropriate settings for memory efficiency
        self.model = AutoModelForCausalLM.from_pretrained(
            model_name,
            load_in_4bit=True,
            torch_dtype=torch.float16 if torch.cuda.is_available() else torch.float32,
            device_map="auto" if torch.cuda.is_available() else None,
            low_cpu_mem_usage=True,
            cache_dir=cache_dir
        )

        if not torch.cuda.is_available():
            self.model = self.model.to(device)

        print(f"Model loaded successfully on {device}")

    def generate_prompt(self, clause: str, use_cot: bool = False) -> str:
        """Generate prompt based on CoT setting"""
        if use_cot:
            prompt = f"""Read the following Terms of Service clause carefully and analyze it step by step.

Consider these aspects:
1. Does it contain vague or ambiguous language about data usage?
2. Does it grant excessive rights to the company?
3. Does it limit user rights or legal recourse?
4. Does it allow data sharing without clear consent?
5. Does it have unclear data retention policies?

Think through each aspect, then determine if this clause is deceptive or legitimate.

Clause: "{clause}"

Analysis: Let me think through this step by step.
"""
        else:
            prompt = f"""Determine if the following Terms of Service clause contains deception or privacy violations.

Clause: "{clause}"

Answer (Deceptive/Legitimate):"""

        return prompt

    def evaluate_clause(self, clause: str, use_cot: bool = False, max_length: int = 256) -> Dict:
        """Evaluate a single clause"""
        prompt = self.generate_prompt(clause, use_cot)

        # Tokenize input
        inputs = self.tokenizer(
            prompt,
            return_tensors="pt",
            truncation=True,
            max_length=512
        ).to(self.model.device)

        # Generate response
        with torch.no_grad():
            outputs = self.model.generate(
                **inputs,
                max_new_tokens=max_length,
                temperature=0.7,
                do_sample=True,
                top_p=0.95,
                pad_token_id=self.tokenizer.pad_token_id
            )

        # Decode response
        response = self.tokenizer.decode(outputs[0], skip_special_tokens=True)

        # Extract the response after the prompt
        response = response[len(prompt):].strip()

        # Parse the response to determine classification
        classification = self.parse_response(response, use_cot)

        return {
            "prompt": prompt,
            "response": response,
            "classification": classification,
            "use_cot": use_cot
        }

    def parse_response(self, response: str, use_cot: bool) -> str:
        """Parse the model response to extract classification"""
        response_lower = response.lower()

        # Look for keywords indicating deceptive or legitimate
        deceptive_keywords = ['deceptive', 'misleading', 'problematic', 'concerning',
                             'unfair', 'violation', 'privacy violation', 'yes']
        legitimate_keywords = ['legitimate', 'acceptable', 'fair', 'transparent',
                              'reasonable', 'no violation', 'no', 'not deceptive']

        # Count keyword occurrences
        deceptive_score = sum(1 for keyword in deceptive_keywords if keyword in response_lower)
        legitimate_score = sum(1 for keyword in legitimate_keywords if keyword in response_lower)

        # Make decision based on scores
        if deceptive_score > legitimate_score:
            return "deceptive"
        elif legitimate_score > deceptive_score:
            return "legitimate"
        else:
            # If unclear, look for specific phrases
            if "deceptive" in response_lower or "violation" in response_lower:
                return "deceptive"
            elif "legitimate" in response_lower or "not deceptive" in response_lower:
                return "legitimate"
            else:
                return "unclear"
